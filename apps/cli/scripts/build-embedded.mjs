import { readdirSync, readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const cliRoot = resolve(here, '..')
const repoRoot = resolve(cliRoot, '..', '..')

function read(rel) {
  return readFileSync(resolve(repoRoot, rel), 'utf8')
}

function escape(value) {
  return value.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${')
}

const compose = read('apps/cli/runtime/compose.yml')
const envExample = read('apps/cli/runtime/.env.example')
const provisionStreams = read('infra/redis/provision-streams.sh')

const migrationsDir = resolve(repoRoot, 'infra/postgres/migrations')
const migrations = readdirSync(migrationsDir)
  .filter((f) => f.endsWith('.up.sql'))
  .sort()
  .map((name) => ({ name, body: readFileSync(join(migrationsDir, name), 'utf8') }))

const lines = []
lines.push('// Copyright (C) 2026 Garudex Labs.  All Rights Reserved.')
lines.push('// Caracal, a product of Garudex Labs')
lines.push('//')
lines.push('// Auto-generated by apps/cli/scripts/build-embedded.mjs. Do not edit by hand.')
lines.push('')
lines.push('export const COMPOSE_YML = `' + escape(compose) + '`')
lines.push('')
lines.push('export const ENV_EXAMPLE = `' + escape(envExample) + '`')
lines.push('')
lines.push('export const PROVISION_STREAMS_SH = `' + escape(provisionStreams) + '`')
lines.push('')
lines.push('export const MIGRATIONS: { name: string; body: string }[] = [')
for (const m of migrations) {
  lines.push(`  { name: '${m.name}', body: \`${escape(m.body)}\` },`)
}
lines.push(']')
lines.push('')

const outDir = resolve(cliRoot, 'src', 'runtime')
if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true })
writeFileSync(resolve(outDir, 'embedded.ts'), lines.join('\n'), 'utf8')
process.stdout.write('caracal: wrote src/runtime/embedded.ts\n')
